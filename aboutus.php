                <h1>Quiénes Somos</h1>
                <p>
                    El proyecto aborda un problema relevante en la sociedad actual, centrándose en la gestión de mascotas en situaciones de adopción, pérdida o encontradas. La falta de una plataforma eficiente para conectar dueños de mascotas, posibles adoptantes y aquellos en busca de sus mascotas perdidas ha generado consecuencias negativas para animales y personas. Esta problemática se manifiesta en un entorno digital y social en constante evolución, destacando la urgencia de una solución integral.
                </p>
                <h2>Definición y Delimitación del Problema:</h2>
                <p>El problema se enfoca en la gestión de mascotas perdidas y en adopción, careciendo de una plataforma unificada. Este abordaje se delimita a situaciones específicas: pérdida, adopción y estado encontrado. La plataforma se centra en permitir a los usuarios publicar información sobre mascotas en estas situaciones, facilitando la comunicación y toma de decisiones.</p>
                <h2>Objetivos del Proyecto:</h2>
                <p>Objetivo General:
                    Desarrollar una aplicación web que permita gestionar información sobre mascotas perdidas y en adopción, facilitando la búsqueda, promoción de adopciones responsables y comunicación efectiva, contribuyendo al bienestar animal y la responsabilidad social.</p>
                <h2>Objetivos Específicos (S.M.A.R.T.):</h2>
                <ul>
                    <li>Incrementar la visibilidad de las mascotas en adopción en un 30% en el primer año.</li>
                    <li>Reducir el tiempo promedio de búsqueda de mascotas perdidas en un 50% en el primer año.</li>
                </ul>
                <h2>Impacto Esperado del Proyecto:</h2>
                <p>La plataforma aspira a mejorar el bienestar animal, reunificar mascotas perdidas más rápidamente, promover la responsabilidad social, fomentar la participación comunitaria y facilitar decisiones informadas sobre adopciones. Los indicadores incluyen aumento porcentual en adopciones, reducción del tiempo de búsqueda, participación en campañas, interacción en comunidades y toma de decisiones informada.

                    La aplicación no solo busca resolver una problemática crucial, sino que también se alinea con competencias del perfil de egreso, aprovechando herramientas técnicas adquiridas en la carrera. Además, la definición clara y los objetivos medibles establecen bases sólidas para evaluar el éxito y el impacto del proyecto.</p>
