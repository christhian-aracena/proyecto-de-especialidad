                <h1>Politicas de Privacidad rescatamigos 2023</h1>
                <h2>1. Información General:</h2>
                <p>Esta página informa a los usuarios sobre nuestras políticas con respecto a la recopilación, uso y divulgación de información personal si alguien decide utilizar nuestro Servicio.</p>

                <h2>2. Recopilación y Uso de Información:</h2>
                <p><strong>Información Personal:</strong></p>
                <p>Al utilizar nuestro Sitio, podemos solicitarle que nos proporcione cierta información de identificación personal que se puede utilizar para contactarlo o identificarlo. La información de identificación personal puede incluir, pero no se limita a, su nombre, dirección de correo electrónico, dirección postal y número de teléfono.</p>

                <p><strong>Datos de Uso:</strong></p>
                <p>También podemos recopilar información sobre cómo se accede y utiliza el Sitio (Datos de Uso). Estos Datos de Uso pueden incluir información como la dirección del Protocolo de Internet de su computadora (por ejemplo, dirección IP), tipo de navegador, versión del navegador, las páginas de nuestro Sitio que visita, la hora y la fecha de su visita, el tiempo dedicado a esas páginas, identificadores de dispositivo y otros datos de diagnóstico.</p>

                <h2>3. Cookies:</h2>
                <p><strong>Cookies y Datos de Sesión:</strong></p>
                <p>Utilizamos cookies y tecnologías de seguimiento similares para rastrear la actividad en nuestro Sitio y almacenar cierta información. Las cookies son archivos con una pequeña cantidad de datos que pueden incluir un identificador único anónimo. Las cookies se envían a su navegador desde un sitio web y se almacenan en su dispositivo. Puede instruir a su navegador para que rechace todas las cookies o para que le avise cuando se envía una cookie. Sin embargo, si no acepta cookies, es posible que no pueda utilizar algunas partes de nuestro Sitio.</p>

                <h2>4. Uso de la Información:</h2>
                <p>Utilizamos la información recopilada con diversas finalidades, que incluyen:</p>
                <ul>
                    <li>Proporcionar y mantener nuestro Servicio.</li>
                    <li>Notificarle sobre cambios en nuestro Servicio.</li>
                    <li>Permite participar en funciones interactivas de nuestro Servicio cuando elija hacerlo.</li>
                    <li>Brindar asistencia al cliente.</li>
                    <li>Recopilar análisis o información valiosa para mejorar nuestro Servicio.</li>
                    <li>Controlar el uso de nuestro Servicio.</li>
                </ul>

                <h2>5. Divulgación de Datos:</h2>
                <p><strong>Requisitos legales:</strong></p>
                <p>Podemos divulgar su información personal en la creencia de buena fe de que dicha acción es necesaria para:</p>
                <ul>
                    <li>Cumplir con una obligación legal.</li>
                    <li>Proteger y defender los derechos o propiedad de [nombre de tu empresa/organización].</li>
                    <li>Prevenir o investigar posibles irregularidades en relación con el Servicio.</li>
                    <li>Proteger la seguridad personal de los usuarios del Servicio o del público.</li>
                    <li>Proteger contra la responsabilidad legal.</li>
                </ul>

                <h2>6. Seguridad:</h2>
                <p>La seguridad de su información personal es importante para nosotros, pero recuerde que ningún método de transmisión a través de Internet o método de almacenamiento electrónico es 100% seguro. Aunque nos esforzamos por utilizar medios comercialmente aceptables para proteger su información personal, no podemos garantizar su seguridad absoluta.</p>

                <h2>7. Cambios en Esta Política de Privacidad:</h2>
                <p>Podemos actualizar nuestra Política de Privacidad de vez en cuando. Le notificaremos cualquier cambio publicando la nueva Política de Privacidad en esta página.</p>

                <h2>8. Contacto:</h2>
                <p>Si tiene alguna pregunta sobre esta Política de Privacidad, contáctenos a través del correo info@rescatamigos.cl.</p>

