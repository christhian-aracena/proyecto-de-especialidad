                <h2>3. Cookies:</h2>
                <p><strong>Cookies y Datos de Sesión:</strong></p>
                <p>Utilizamos cookies y tecnologías de seguimiento similares para rastrear la actividad en nuestro Sitio y almacenar cierta información. Las cookies son archivos con una pequeña cantidad de datos que pueden incluir un identificador único anónimo. Las cookies se envían a su navegador desde un sitio web y se almacenan en su dispositivo. Puede instruir a su navegador para que rechace todas las cookies o para que le avise cuando se envía una cookie. Sin embargo, si no acepta cookies, es posible que no pueda utilizar algunas partes de nuestro Sitio.</p>

                <h2>4. Uso de la Información:</h2>
                <p>Utilizamos la información recopilada con diversas finalidades, que incluyen:</p>
                <ul>
                    <li>Proporcionar y mantener nuestro Servicio.</li>
                    <li>Notificarle sobre cambios en nuestro Servicio.</li>
                    <li>Permite participar en funciones interactivas de nuestro Servicio cuando elija hacerlo.</li>
                    <li>Brindar asistencia al cliente.</li>
                    <li>Recopilar análisis o información valiosa para mejorar nuestro Servicio.</li>
                    <li>Controlar el uso de nuestro Servicio.</li>
                </ul>

                <h2>5. Divulgación de Datos:</h2>
                <p><strong>Requisitos legales:</strong></p>
